#Create a list
fruits = ["apples", "bananas", "cherries"]

#Print the list
print(fruits) #Output: ['apples', 'bananas', 'cherries']

#Check the type of list
print(type(fruits)) #Output: <class 'list'>

#Length of the list
print(len(fruits)) #Output: 3

#Check if an item is present in the list
if "bananas" in fruits:
    print("Bananas are a part of the list.")

#Check if an item is not present in the list
if "grapes" not in fruits:
    print("Grapes are not a part of the list.")