#For Loop - Example 1 (Inclusive Start and Exclusive End)
for i in range(1, 6):
    print("Hello, World!")

'''Output:
Hello, World!
Hello, World!
Hello, World!
Hello, World!
Hello, World!
'''

#For Loop - Example 2 (Inclusive Start, Exclusive End, Step)
for i in range(1, 11, 2):
    print("Hello, Python!")

'''Output:
Hello, Python!
Hello, Python!
Hello, Python!
Hello, Python!
Hello, Python!
'''

#For Loop - Example 3 (End Only)
for i in range(6):
    print(i, "Hello, Programming!")

'''Output:
0 Hello, Programming!
1 Hello, Programming!
2 Hello, Programming!
3 Hello, Programming!
4 Hello, Programming!
5 Hello, Programming!
'''