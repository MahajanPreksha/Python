#Taking temperature in Celsius as input
celcius_temp = float(input("Enter temperature in Celsius: "))

#Converting Celsius to Fahrenheit
fahrenheit_temp = (celcius_temp * 9 / 5) + 32

#Printing temperature in Fahrenheit
print("Temperature in Fahrenheit:", fahrenheit_temp)

'''Output:
Enter temperature in Celsius: 20
Temperature in Fahrenheit: 68.0
'''